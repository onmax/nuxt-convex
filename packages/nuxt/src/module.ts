interface BetterAuthDatabaseProviderDefinition {
  buildDatabaseCode: (ctx: unknown) => string
  setup?: (ctx: unknown) => void | Promise<void>
}

type BetterAuthDatabaseProviders = Record<string, BetterAuthDatabaseProviderDefinition>

const module: NuxtModule<ConvexConfig> = defineNuxtModule<ConvexConfig>({
  meta: {
    name: 'nuxt-convex',
    version,
    configKey: 'convex',
    compatibility: { nuxt: '>=3.0.0' },
  },
  defaults: {
    dir: 'convex',
    storage: false,
    r2: false,
    server: true,
  },
  async onInstall(nuxt) {
    const options = nuxt.options.convex as ConvexConfig | undefined
    if (options?.storage)
      await scaffoldStorageFunctions(nuxt)
  },
  async setup(options, nuxt) {
    const { resolve } = createResolver(import.meta.url)
    const config = resolveConfig(options)

    nuxt.options.runtimeConfig.public.convex = defu(
      nuxt.options.runtimeConfig.public.convex as Record<string, unknown> | undefined,
      {
        dir: config.dir,
        url: config.url,
        r2: config.r2,
        storage: config.storage,
        server: config.server,
      },
    )

    nuxt.hook('better-auth:database:providers', (providers: BetterAuthDatabaseProviders) => {
      addPlugin(resolve('./runtime/better-auth/plugin'))
      providers.convex = {
        buildDatabaseCode: () => `import { useRuntimeConfig } from '#imports'
import { createConvexHttpAdapter } from 'nuxt-convex/better-auth'
import { api } from '#convex/api'

export function createDatabase() {
  const config = useRuntimeConfig()
  const convexUrl = config.betterAuth?.convexUrl || config.public?.convex?.url
  if (!convexUrl) {
    throw new Error('[nuxt-better-auth] Convex URL not configured. Set auth.database.convexUrl, convex.url, or CONVEX_URL.')
  }
  return createConvexHttpAdapter({ url: convexUrl, api: api.auth })
}
export const db = undefined
`,
        setup: (ctx) => {
          const hookNuxt = (ctx as { nuxt?: Nuxt } | undefined)?.nuxt
          if (!hookNuxt)
            return

          const convexUrl = (hookNuxt.options as { auth?: { database?: { convexUrl?: string } } }).auth?.database?.convexUrl
          if (!convexUrl)
            return

          hookNuxt.options.runtimeConfig.betterAuth = defu(
            (hookNuxt.options.runtimeConfig.betterAuth as Record<string, unknown> | undefined) || {},
            { convexUrl },
          )
        },
      }
    })

    addPlugin(resolve('./runtime/plugin'))
    setupConvexApiAlias(nuxt)
    setupConvexExports(nuxt, resolve)
    setupConvexAdvanced(nuxt, resolve)
    setupInternalStorageRefsRuntime(nuxt)

    if (config.storage) {
      setupConvexStorage(nuxt, resolve)
      await scaffoldStorageFunctions(nuxt)
    }
    else {
      setupStorageRuntimeFallback(nuxt, resolve)
    }

    if (config.r2)
      setupConvexR2(nuxt, resolve)

    if (nuxt.options.dev)
      addConvexDevTools(nuxt, config)

    nuxt.hook('ready', () => {
      if (config.url)
        log.info(`Convex connected: ${config.url}`)
      else
        log.warn('Convex URL not configured. Set CONVEX_URL or NUXT_PUBLIC_CONVEX_URL')
    })
  },
})

export default module

function resolveConfig(options: ConvexConfig): ResolvedConvexConfig {
  const url = options.url
    || process.env.CONVEX_URL
    || process.env.NUXT_PUBLIC_CONVEX_URL
    || ''

  return defu(options, { url, dir: 'convex', storage: false, r2: false, server: true }) as ResolvedConvexConfig
}

function setupConvexExports(nuxt: Nuxt, resolve: (path: string) => string): void {
  const convexVueRuntimeImport = resolveConvexVueImport(nuxt, resolve, join(nuxt.options.buildDir, 'convex'), 'index')
  const convexVueTypeImport = resolveConvexVueImport(nuxt, resolve, join(nuxt.options.buildDir, 'types'), 'index')
  const template = addTemplate({
    filename: 'convex/index.mjs',
    getContents: () => `// Auto-generated by nuxt-convex
export * from '${convexVueRuntimeImport}'
`,
    write: true,
  })

  addTypeTemplate({
    filename: 'types/convex.d.ts',
    getContents: () => `// Auto-generated by nuxt-convex
declare module '#convex' {
  export * from '${convexVueTypeImport}'
}
`,
  }, { nitro: true, nuxt: true })

  nuxt.options.alias['#convex'] = template.dst

  addImports(rootAutoImports.map(name => ({ name, from: '#convex' })))

  addComponent({ name: 'ConvexQuery', export: 'default', filePath: resolve('./runtime/components/ConvexQuery'), global: true })
  addComponent({ name: 'ConvexPaginatedQuery', export: 'default', filePath: resolve('./runtime/components/ConvexPaginatedQuery'), global: true })

  for (const name of ['ConvexAuthenticated', 'ConvexUnauthenticated', 'ConvexAuthLoading'] as const)
    addComponent({ name, export: name, filePath: resolve('./runtime/components/ConvexAuth'), global: true })
}

