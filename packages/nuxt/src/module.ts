function resolveConvexVueImport(
  nuxt: Nuxt,
  resolve: (path: string) => string,
  fromDir: string,
  entry: 'advanced' | 'index' | 'storage',
): string {
  const localSourceEntry = resolve(`../../vue/src/${entry}`)
  if (existsSync(localSourceEntry) || existsSync(`${localSourceEntry}.ts`) || existsSync(`${localSourceEntry}.js`))
    return toImportPath(fromDir, localSourceEntry)

  return entry === 'index' ? '@onmax/convex-vue' : `@onmax/convex-vue/${entry}`
}

async function scaffoldStorageFunctions(nuxt: Nuxt): Promise<void> {
  const convexRoot = resolveConvexRoot(nuxt, (nuxt.options.convex as ConvexConfig | undefined)?.dir || 'convex')
  const storageDir = join(convexRoot.writeDir, '_hub')
  const storagePath = join(storageDir, 'storage.ts')
  const generatedServerImport = resolveStorageServerImportPath(convexRoot)

  if (existsSync(storagePath))
    return

  if (convexRoot.fallsBackToProjectWrite) {
    log.warn(`Reading Convex files from ${formatProjectRelativePath(nuxt, convexRoot.readDir)} and scaffolding ${formatProjectRelativePath(nuxt, storagePath)} instead because the resolved layer is not writable. Set \`convex.dir\` to a writable project path to override this.`)
  }

  await mkdir(storageDir, { recursive: true })
  await writeFile(storagePath, `// Auto-generated by nuxt-convex - feel free to customize
import { v } from 'convex/values'
import { mutation, query } from '${generatedServerImport}'

export const generateUploadUrl = mutation(async (ctx) => {
  return await ctx.storage.generateUploadUrl()
})

export const getUrl = query({
  args: { storageId: v.id('_storage') },
  handler: async (ctx, { storageId }) => {
    return await ctx.storage.getUrl(storageId)
  },
})

export const remove = mutation({
  args: { storageId: v.id('_storage') },
  handler: async (ctx, { storageId }) => {
    await ctx.storage.delete(storageId)
  },
})
`)
  log.success(`Created ${formatProjectRelativePath(nuxt, storagePath)}`)
}

function setupConvexR2(nuxt: Nuxt, resolve: (path: string) => string): void {
  addImports({ name: 'useConvexR2Upload', from: resolve('./runtime/composables/useConvexR2Upload') })
}

interface DevToolsCustomTab {
  name: string
  title: string
  icon?: string
  category?: string
  view: { type: 'iframe', src: string }
}

function addConvexDevTools(nuxt: Nuxt, config: ResolvedConvexConfig): void {
  nuxt.hook('devtools:customTabs', (tabs: DevToolsCustomTab[]) => {
    if (!config.url)
      return

    const dashboardUrl = config.url.replace('.convex.cloud', '.convex.dev')

    tabs.push({
      category: 'server',
      name: 'convex',
      title: 'Convex',
      icon: 'i-simple-icons-convex',
      view: { type: 'iframe', src: dashboardUrl },
    })
  })
}
