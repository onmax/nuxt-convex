function setupConvexApiAlias(nuxt: Nuxt): void {
  const convexRoot = resolveConvexRoot(nuxt, (nuxt.options.convex as ConvexConfig | undefined)?.dir || 'convex')
  const generatedDir = convexRoot.generatedDir

  if (!existsSync(generatedDir))
    log.warn(`Missing ${formatProjectRelativePath(nuxt, generatedDir)}. Run \`npx convex dev\` to generate types.`)

  nuxt.options.alias['#convex/api'] = join(generatedDir, 'api')
  const generatedApiImport = toImportPath(join(nuxt.options.buildDir, 'types'), join(generatedDir, 'api'))

  addTypeTemplate({
    filename: 'types/convex-api.d.ts',
    getContents: () => `// Auto-generated by nuxt-convex
declare module '#convex/api' {
  export { api, internal, components } from '${generatedApiImport}'
}
`,
  }, { nitro: true, nuxt: true })
}

